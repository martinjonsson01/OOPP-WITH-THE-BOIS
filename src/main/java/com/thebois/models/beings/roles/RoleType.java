package com.thebois.models.beings.roles;

/**
 * Describes which different types of roles there are.
 *
 * @author Martin
 */
public enum RoleType {
    /**
     * Does nothing.
     */
    IDLE,
    /**
     * Builds structures.
     */
    BUILDER,
    /**
     * Harvests crops.
     */
    FARMER,
    /**
     * Catches fish.
     */
    FISHER,
    /**
     * Defends from attacks.
     */
    GUARD,
    /**
     * Chops down trees.
     */
    LUMBERJACK,
    /**
     * Digs up rocks.
     */
    MINER
}
